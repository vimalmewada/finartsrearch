                        <p class="fw-semibold">D. Details of grievance redressal mechanism and how to access it</p>
                        <p>
                            In case of any grievance or complaint, an investor should approach the concerned research
                            analyst and ensure that the grievance is resolved within 21 days of receipt.
                        </p>

                        <ul class="list-unstyled ms-4">
                            <li class="mb-3">
                                <strong>Mode of filing the complaint with the research analyst:</strong>
                                <p>
                                    In case of grievance, an investor may approach the concerned Research Analyst who
                                    will strive to resolve the grievance immediately, but not later than 21 days of
                                    receiving the complaint.
                                </p>
                            </li>

                            <li class="mb-3">
                                <strong>Mode of filing the complaint on SCORES or with Research Analyst Administration
                                    and Supervisory Body (RAASB):</strong>
                                <ul class="ms-4">
                                    <li><strong>SCORES 2.0:</strong> A web-based centralized grievance redressal system
                                        by SEBI. It facilitates effective grievance redressal in a time-bound manner.
                                        [Link: <a href="https://scores.sebi.gov.in" target="_blank">SCORES Portal</a>]
                                    </li>
                                    <li>
                                        <strong>Two-level review for complaints/grievances against Research
                                            Analysts:</strong>
                                        <ul class="ms-4">
                                            <li>First review by the designated body (RAASB)</li>
                                            <li>Second review by SEBI</li>
                                        </ul>
                                    </li>
                                    <li><strong>Email to designated email ID of RAASB:</strong> Complaints can also be
                                        filed via email.</li>
                                </ul>
                            </li>

                            <li class="mb-3">
                                <strong>If not satisfied with the resolution from Market Participants:</strong>
                                <p>
                                    The investor has the option to file the complaint/grievance on the SMARTODR platform
                                    for resolution through online conciliation or arbitration.
                                </p>
                            </li>

                            <li class="mb-3">
                                <strong>For physical complaints, investors may send them to:</strong>
                                <address>
                                    Office of Investor Assistance and Education<br>
                                    Securities and Exchange Board of India<br>
                                    SEBI Bhavan, Plot No. C4-A, ‘G’ Block<br>
                                    Bandra-Kurla Complex, Bandra (East)<br>
                                    Mumbai – 400051, Maharashtra<br>
                                </address>
                            </li>
                        </ul>


                        <p class="fw-semibold">E. Rights of Investors</p>
                        <ul class="list-unstyled ms-4">
                            <li class="mb-2">&#10003; Right to Privacy and Confidentiality</li>
                            <li class="mb-2">&#10003; Right to Transparent Practices</li>
                            <li class="mb-2">&#10003; Right to Fair and Equitable Treatment</li>
                            <li class="mb-2">&#10003; Right to Adequate Information</li>
                            <li class="mb-2">&#10003; Right to Initial and Continuing Disclosure</li>
                            <li class="mb-2">&#10003; Right to Receive Information about all Statutory and Regulatory Disclosures
                            </li>
                            <li class="mb-2">&#10003; Right to Fair & True Advertisement</li>
                            <li class="mb-2">&#10003; Right to Awareness about Service Parameters and Turnaround Times</li>
                            <li class="mb-2">&#10003; Right to be Informed of the Timelines for Each Service</li>
                            <li class="mb-2">&#10003; Right to be Heard and Satisfactory Grievance Redressal</li>
                            <li class="mb-2">&#10003; Right to Have Timely Redressal</li>
                            <li class="mb-2">&#10003; Right to Exit from Financial Products or Services in Accordance with the
                                Terms and Conditions Agreed with the Research Analyst</li>
                            <li class="mb-2">&#10003; Right to Receive Clear Guidance and Caution Notice When Dealing in Complex
                                and High-Risk Financial Products and Services</li>
                            <li class="mb-2">Additional Rights for Vulnerable Consumers:</li>
                            <ul class="ms-4">
                                <li class="mb-2"> Right to Access Services in a Suitable Manner, Even if Differently
                                    Abled</li>
                            </ul>
                            <li class="mb-2">&#10003; Right to Provide Feedback on the Financial Products and Services Used</li>
                            <li class="mb-2">&#10003; Right Against Coercive, Unfair, and One-Sided Clauses in Financial
                                Agreements</li>
                        </ul>


                       
