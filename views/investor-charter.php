        <h4 class="fw-bold">Do’s</h4>
        <hr>
        <ul class="list-unstyled ms-3">
            <li class="mb-2">✔️ Always deal with SEBI registered Research Analyst.</li>
            <li class="mb-2">✔️ Ensure that the Research Analyst has a valid registration certificate.</li>
            <li class="mb-2">✔️ Check for SEBI registration number.</li>
            <li class="mb-2">
                ✔️ Please refer to the list of all SEBI registered Research Analysts which is available on SEBI website:<br>
                <a href="https://www.sebi.gov.in/sebiweb/other/OtherAction.do?doRecognisedFpi=yes&intmId=14" target="_blank" rel="noopener noreferrer">
                    https://www.sebi.gov.in/sebiweb/other/OtherAction.do?doRecognisedFpi=yes&intmId=14
                </a>
            </li>
            <li class="mb-2">✔️ Always pay attention towards disclosures made in the research reports before investing.</li>
            <li class="mb-2">✔️ Pay your Research Analyst through banking channels only and maintain duly signed receipts mentioning the details of your payments.</li>
            <li class="mb-2">✔️ You may make payment of fees through Centralized Fee Collection Mechanism (CeFCoM) of RAASB if the Research Analyst has opted for it. (Applicable for fee-paying clients only)</li>
            <li class="mb-2">✔️ Before buying/selling securities or applying in public offer, check for the research recommendation provided by your Research Analyst.</li>
            <li class="mb-2">✔️ Ask all relevant questions and clear your doubts with your Research Analyst before acting on recommendations.</li>
            <li class="mb-2">✔️ Seek clarifications and guidance on research recommendations from your Research Analyst, especially if it involves complex and high-risk financial products and services.</li>
            <li class="mb-2">✔️ Always be aware that you have the right to stop availing the service of a Research Analyst as per the agreed terms of service.</li>
            <li class="mb-2">✔️ Always be aware that you have the right to provide feedback to your Research Analyst regarding the services received.</li>
            <li class="mb-2">✔️ Always be aware that you are not bound by any clause prescribed by the Research Analyst which contravenes any regulatory provisions.</li>
            <li class="mb-2">✔️ Inform SEBI about any Research Analyst offering assured or guaranteed returns.</li>
        </ul>
    </li>

    <li class="mb-4">
        <h4 class="fw-bold">Don’ts</h4>
        <hr>
        <ul class="list-unstyled ms-3">
            <li class="mb-2">❌ Do not provide funds for investment to the Research Analyst.</li>
            <li class="mb-2">❌ Don’t fall prey to luring advertisements or market rumours.</li>
            <li class="mb-2">❌ Do not get attracted to limited-period discounts or other incentives, gifts, etc., offered by Research Analyst.</li>
            <li class="mb-2">❌ Do not share login credentials and passwords of your trading, demat, or bank accounts with the Research Analyst.</li>
